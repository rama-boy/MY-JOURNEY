# list -> array, access by using index
data_list = ['rama','ikrama','bana']
print(data_list[0])

# Dictionary (dict) -> associative array
# identifier -> identity : key
data_dict = {
    'key' : 'value',
    'rm' : 'rama',
    'ikr' : 'ikrama',
    'bn' : 'bana',
    'nmbr':100,
    'list' : data_list
}
print(data_dict['rm'])
print(data_dict['nmbr'])
print(data_dict['list'])
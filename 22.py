# ELIF = else if statement

name = input("What is your name? ")

if name == "rama": # condition 1
	print("Hi boy!") # true action 1
elif name == "ikrama": # condition 2
	print("Hi, wasap broh!") # true action 2
elif name == "bana": # condition 3
	print("Welcome!") # true action 3
else:
	print("please leave, you are not invited!!!") # false action

print("END PROGRAM")